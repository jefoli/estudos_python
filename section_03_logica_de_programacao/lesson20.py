#aula20 - Tipo int e float (números) - Introdução aos tipos de dados

#tipos int e float

# int -> Número inteiro
# o tipo int representa qualquer número
#positivo ou negativo

#OBS: se o int não possuir sinal, por padrão é reconhecido como POSITIVO

print(11) #esse é um número literal inteiro, pois passamos o valor dentro do print

print(-11)

print(0)





#float -> Número com ponto flutuante
#usamos um ponto com número decimal
# pode ser 0.0
print(1.1, 10.11)
print(0.0, -1.5)

#type -> mostra o tipo que o python
#inferiu o valor

#type é uma classe, porém como possui letra maíuscula(é considerada função)

print(type ('Otávio'))
print(type (1))
print(type (-1))
print(type (0))
print(type (1.1))
print(type (1.1), type(-1.1))

